public class Atividade3 {
    public static void main(String[] args) {
        int sequencia = 233;
        System.out.println(sequencia);
        while (sequencia < 300) {
            sequencia += 5;
            System.out.println(sequencia);
        }
        while (sequencia <= 400) {
            sequencia += 3;
            System.out.println(sequencia);
        }    
        while (sequencia < 456) {
            sequencia += 5;
            System.out.println(sequencia);
        }
    }
}
